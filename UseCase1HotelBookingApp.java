/**
 * UseCase1HotelBookingApp represents the entry point for the Hotel Booking Management System.
 * This class establishes a clear starting point for the application by demonstrating 
 * how a Java program begins execution and produces console output.
 * 
 * @author Book My Stay Team
 * @version 1.0
 */
public class UseCase1HotelBookingApp {

    /**
     * The main method is the entry point of the standalone Java application.
     * The JVM looks specifically for this method signature to start execution.
     * 
     * @param args command-line arguments (not used)
     */
    public static void main(String[] args) {
        // Welcome message along with the application name and version
        System.out.println("Welcome to the Hotel Booking Management System!");
        System.out.println("Application Name: Hotel Booking System");
        System.out.println("Version: 1.0");
        
        // The application terminates after the last statement in main()
    }
}
